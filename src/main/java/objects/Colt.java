package hello;

public class Colt {

    private String make;
    private String model;
    private boolean automaticAction;
    private int clipCapacity;

    public Colt(String model){
        this.model = model;
    }



    public String getMake() {
        return make;
    }

    public String getModel() {
        return model;
    }

    public boolean isAutomaticAction() {
        return automaticAction;
    }

    public int getClipCapacity() {
        return clipCapacity;
    }






}
